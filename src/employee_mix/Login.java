    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //login button
      
    String username = jTextField1.getText();
    String password = new String(jPasswordField1.getPassword());
    
    //splite kore read korsi.array te set kore match korsi
    if (username.equals("admin") && "admin123".equals(password)) {
        // If the entered credentials match the admin credentials
        JOptionPane.showMessageDialog(this, "Admin login successful!");
        new FrontPage().setVisible(true);
        this.dispose();
    } else {
        try {
            BufferedReader reader = new BufferedReader(new FileReader("users_account.txt"));
            String line;
            boolean userFound = false;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(":");
                if (parts[0].equals(username)) {
                    if (parts[1].equals(password)) {
                        JOptionPane.showMessageDialog(this, "Login successful!");
                        new FrontPage().setVisible(true);
                        
                    } else {
                        JOptionPane.showMessageDialog(this, "Incorrect username or password. Please try again.");
                    }
                    userFound = true;
                    break;
                }
            }
            if (!userFound) {
                JOptionPane.showMessageDialog(this, "User not found. Please try again.");
            }
            reader.close();
            this.dispose();
        } catch (IOException e) {}
    
     }
        
    }//GEN-LAST:event_jButton2ActionPerformed


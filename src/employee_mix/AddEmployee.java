    public void saveEmployeesToFile(){
        
        try{
            FileOutputStream file = new FileOutputStream("Employee.dat");//reading from that file
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            //for every single job object
            for(int i=0 ; i < employees.size() ; i++){
                outputFile.writeObject(employees.get(i));//saving the job arraylist for every jobs object
            }
            outputFile.close();//close korlam
            
            JOptionPane.showMessageDialog(null, "Successfully saved");
            this.dispose();//close the window easy
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    
    

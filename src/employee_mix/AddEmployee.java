    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //save butt
        
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please fill the all informations!");
        }
        else
        {
            String name = jTextField1.getText().trim();
            String lastname = jTextField2.getText().trim();
            int jobIndex = jComboBox1.getSelectedIndex();
            Job job = jobs.get(jobIndex);
            int employee_id = Integer.parseInt(jTextField3.getText().trim());
            
            
            Employee employee = new Employee(name,lastname,job,employee_id);
            
            employees.add(employee);//employee arraylist adding
            
            saveEmployeesToFile();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


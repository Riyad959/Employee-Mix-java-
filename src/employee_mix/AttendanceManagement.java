    public void saveAttendanceToFile(){
        
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            
            JOptionPane.showMessageDialog(null, "Please fill the all informations!");
            
        } else {
            try {
                FileWriter writer = new FileWriter("Attendance.csv", true);
            writer.append(jComboBox1.getSelectedItem().toString());
            writer.append(',');
            writer.append(jTextField2.getText());
            writer.append(',');
            writer.append(jTextField1.getText());
            writer.append(',');
            writer.append(jComboBox2.getSelectedItem().toString());
            writer.append('\n');
            writer.flush();//flush writer kora hoise - stackoverflow
            writer.close();
            JOptionPane.showMessageDialog(null, "Attendance saved successfully!");
            dispose();// close
                
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error to save file!");
            }
        }
    
    }

    public void populateArraylist(){
        
        //for employee
        try{
            FileInputStream file2 =new FileInputStream("Employee.dat");//data file create and save- stackoverflow
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);//read from the file
            
            boolean endOfFile =false;
            
            while(!endOfFile){
                try{
                    employees.add((Employee)inputFile2.readObject());//convertd job object adding to the arraylist
                }
                catch(EOFException e){
                    endOfFile = true;
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile2.close();
        }
        catch(IOException r){
            JOptionPane.showMessageDialog(null, r.getMessage());
        }

    }


